/*
  Kata-name => Convert boolean values to strings 'Yes' or 'No'

Description => Complete the method that takes a boolean value and return a "Yes" string for true, or a "No" string for false.

*/ //=========================================================

function boolToWordFirst(bool) {
  return bool ? "Yes" : "No";
}

