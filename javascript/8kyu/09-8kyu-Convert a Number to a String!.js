/*
  Kata-name => Convert a Number to a String!

Description => We need a function that can transform a number into a string

*/ //=========================================================

function numberToStringFirst(num) {
  return "" + num;
}

function numberToStringSecond(num) {
  return num.toString();
}

function numberToStringThird(num) {
  return String(num);
}

function numberToStringForth(num) {
  return `${num}`;
}
